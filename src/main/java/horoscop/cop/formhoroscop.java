    private void bttncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttncalcularActionPerformed
        // TODO add your handling code here:

        int dia=Integer.parseInt(txtdia.getText());
        String mes=(String)mescombox.getSelectedItem();

        if(dia>=21 && mes.equals("Marzo") ||dia<=20 && mes.equals("Abril")) {
            resultado.setText("Aries");
        }else if(dia>=21 && mes.equals("Abril") || dia<=20 && mes.equals("Mayo")){
            resultado.setText("Tauro"); 
        }else if(dia>=21 && mes.equals("Mayo") || dia<=20 && mes.equals("Junio")){
            resultado.setText("Geminis");
        }else if(dia>=21 && mes.equals("Junio") || dia<=20 && mes.equals("Julio")){
            resultado.setText("Cancer");
        }else if(dia>=21 && mes.equals("Julio") || dia<=20 && mes.equals("Agosto")){
            resultado.setText("Leo");
        }else if(dia>=21 && mes.equals("Agosto") || dia<=20 && mes.equals("Septiembre")){
            resultado.setText("Virgo");
        }else if(dia>=21 && mes.equals("Septiemrbe") || dia<=20 && mes.equals("Octubre")){
            resultado.setText("Libra");
        }else if(dia>=21 && mes.equals("Octubre") || dia<=20 && mes.equals("Noviembre")){
            resultado.setText("Escorpio");
        }else if(dia>=21 && mes.equals("Noviembre") || dia<=20 && mes.equals("Diciembre")){
            resultado.setText("Sagitario");
        }else if(dia>=21 && mes.equals("Diciembre") || dia<=20 && mes.equals("Enero")){
            resultado.setText("Capricornio");
        }else if(dia>=21 && mes.equals("Enero") || dia<=20 && mes.equals("Febrero")){
            resultado.setText("Acuario");
        }else if(dia>=21 && mes.equals("Febrero") || dia<=20 && mes.equals("Marzo")){
            resultado.setText("Piscis");
        }

    }//GEN-LAST:event_bttncalcularActionPerformed

